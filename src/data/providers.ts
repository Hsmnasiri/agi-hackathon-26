export function getProvider(id: string): Provider {
  const p = PROVIDERS.find((x) => x.id === id);
  if (!p) throw new Error(`Unknown provider: ${id}`);
  return p;
}

export function getProvidersByIds(ids: string[]): Provider[] {
  return ids.map(getProvider);
}
